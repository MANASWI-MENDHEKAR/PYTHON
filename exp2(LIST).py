# CREATION OF LIST

list=[1,3,5,2,7,5,8,"apple"]
print(list)

# INDEXING OF LIST

colours = ["red", "blue", "green", "orange","pink"]
print(colours[2])

# SLICING OF LIST

colours = ["red", "blue", "green", "orange","pink"]
print(colours[1:4])

# CONCATINATION OF LIST

colours = ["red", "blue", "green", "orange","pink"]
fruits = ["apple","berry","jaam","santra","lichi"]
print(colours+fruits)

# REPETITON OF LIST

colours = ["red", "blue", "green", "orange","pink"]
print(colours*3)
